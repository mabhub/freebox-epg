import { writeFileSync, mkdirSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const target = process.env.FREEBOX_API_TARGET;

if (!target) {
  console.error('❌ FREEBOX_API_TARGET is required to generate public/_redirects');
  console.error('   Set it in your Netlify site environment variables (or shell for local builds).');
  process.exit(1);
}

try {
  const url = new URL(target);
  if (!['http:', 'https:'].includes(url.protocol)) {
    throw new Error(`unsupported protocol: ${url.protocol}`);
  }
} catch (error) {
  console.error(`❌ FREEBOX_API_TARGET is not a valid URL: ${target}`);
  console.error(`   ${error.message}`);
  process.exit(1);
}

const normalized = target.replace(/\/$/, '');

const content = `# Generated by scripts/generate-redirects.js — do not edit or commit.
/api/latest/*  ${normalized}/api/latest/:splat  200
/*             /index.html                      200
`;

const here = dirname(fileURLToPath(import.meta.url));
const outPath = resolve(here, '..', 'public', '_redirects');

mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(outPath, content, 'utf8');

console.log(`✅ Wrote ${outPath}`);
